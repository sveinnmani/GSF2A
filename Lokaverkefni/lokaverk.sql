SELECT MOVIE_TITLE, MOVIE_YEAR, MOVIE_COST FROM MOVIE
WHERE MOVIE_TITLE LIKE '%hope%'

/*Dæmi 4*/
SELECT MOVIE_TITLE, MOVIE_YEAR, MOVIE_GENRE FROM MOVIE
WHERE MOVIE_GENRE = 'Action'

/*Dæmi 5*/
SELECT MOVIE_NUM, MOVIE_TITLE, MOVIE_COST FROM MOVIE
WHERE MOVIE_COST >= 40 

/*Dæmi 6*/
SELECT MOVIE_NUM, MOVIE_TITLE, MOVIE_COST, MOVIE_GENRE FROM MOVIE
WHERE (MOVIE_GENRE = 'Action' OR MOVIE_GENRE = 'Comedy') OR (MOVIE_COST <= 50)
ORDER BY MOVIE_GENRE

/*Dæmi 7*/
SELECT MOVIE_NUM, CONCAT(MOVIE_TITLE, MOVIE_YEAR) AS "MOVIE"
FROM MOVIE

/*Dæmi 8*/
SELECT MOVIE_GENRE, COUNT(*) AS "NUMBER OF MOVIES"
FROM MOVIE
GROUP BY MOVIE_GENRE

/*Dæmi 9*/
SELECT AVG(MOVIE_COST) AS "AVERAGE MOVIE COST"
FROM MOVIE

/*Dæmi 10*/
SELECT MOVIE_GENRE, AVG(MOVIE_COST) AS "AVERAGE_COST"
FROM MOVIE
GROUP BY MOVIE_GENRE

/*Dæmi 11*/
SELECT MOVIE_TITLE, MOVIE_GENRE, PRICE_DESCRIPTION, PRICE_RENTFEE
FROM MOVIE, PRICE
WHERE MOVIE.PRICE_CODE = PRICE.PRICE_CODE

/*Dæmi 12*/
SELECT MOVIE_GENRE, AVG(PRICE_RENTFEE) AS "AVERAGE RENTAL FEE"
FROM MOVIE, PRICE
WHERE MOVIE.PRICE_CODE = PRICE.PRICE_CODE
GROUP BY MOVIE_GENRE

/*Dæmi 13*/
SELECT MOVIE_TITLE, MOVIE_YEAR, MOVIE_COST / PRICE_RENTFEE AS "BREAKEVEN RENTALS"
FROM MOVIE, PRICE
WHERE MOVIE.PRICE_CODE = PRICE.PRICE_CODE

/*Dæmi 14*/
SELECT MOVIE_TITLE, MOVIE_YEAR
FROM MOVIE, PRICE
WHERE MOVIE.PRICE_CODE = PRICE.PRICE_CODE

/*Dæmi 15*/
SELECT MOVIE_TITLE, MOVIE_YEAR, MOVIE_COST
FROM MOVIE
WHERE MOVIE_COST BETWEEN 44.99 AND 49.99 

/*Dæmi 16*/
SELECT MOVIE_TITLE, MOVIE_YEAR, PRICE_DESCRIPTION, PRICE_RENTFEE, MOVIE_GENRE
FROM MOVIE, PRICE
WHERE MOVIE_GENRE = "COMEDY"
OR MOVIE_GENRE = "DRAMA"
OR MOVIE_GENRE = "FAMILY"
GROUP BY MOVIE_TITLE

/*Dæmi 17*/
SELECT MIN(MEM_BALANCE) AS "MINIMUM BALANCE", MAX(MEM_BALANCE) AS "MAXIMUM BALANCE", AVG(MEM_BALANCE) AS "AVERAGE BALANCE"
FROM MEMBERSHIP

/*Dæmi 18*/
SELECT CONCAT(MEM_FNAME, ' ', MEM_LNAME) AS "MEMBERSHIP NAME", CONCAT(MEM_STREET, ', ',  MEM_CITY, ', ', MEM_STATE, ' ', MEM_ZIP) AS "MEMBERSHIP ADDRESS"
FROM MEMBERSHIP

/*Dæmi 19*/
SELECT RENTAL.RENT_NUM, RENT_DATE, VIDEO.VID_NUM, MOVIE_TITLE, DETAIL_DUEDATE, DETAIL_RETURNDATE
FROM RENTAL, DETAILRENTAL, VIDEO, MOVIE 
WHERE RENTAL.RENT_NUM = DETAILRENTAL.RENT_NUM 
AND DETAILRENTAL.VID_NUM = VIDEO.VID_NUM 
AND VIDEO.MOVIE_NUM = MOVIE.MOVIE_NUM 
AND DETAIL_DUEDATE < DETAIL_RETURNDATE
ORDER BY RENTAL.RENT_NUM, MOVIE_TITLE

/*Dæmi 20*/
SELECT RENTAL.RENT_NUM, RENT_DATE, VIDEO.VID_NUM, MOVIE_TITLE, DETAIL_DUEDATE, DETAIL_RETURNDATE, DETAIL_RETURNDATE - DETAIL_DUEDATE AS "DAYS PAST DUE DATE"
FROM RENTAL, DETAILRENTAL, VIDEO, MOVIE 
WHERE RENTAL.RENT_NUM = DETAILRENTAL.RENT_NUM 
AND DETAILRENTAL.VID_NUM = VIDEO.VID_NUM 
AND VIDEO.MOVIE_NUM = MOVIE.MOVIE_NUM 
AND DETAIL_DUEDATE < DETAIL_RETURNDATE
ORDER BY `DAYS PAST DUE DATE`  DESC

/*Dæmi 21*/
SELECT RENTAL.RENT_NUM, RENT_DATE, MOVIE_TITLE, DETAIL_FEE
FROM RENTAL, DETAILRENTAL, VIDEO, MOVIE 
WHERE RENTAL.RENT_NUM = DETAILRENTAL.RENT_NUM 
AND DETAILRENTAL.VID_NUM = VIDEO.VID_NUM 
AND VIDEO.MOVIE_NUM = MOVIE.MOVIE_NUM 
AND DETAIL_DUEDATE >= DETAIL_RETURNDATE
ORDER BY RENTAL.RENT_NUM, MOVIE_TITLE

/*Dæmi 22*/
SELECT MEMBERSHIP.MEM_NUM, MEM_LNAME, MEM_FNAME, SUM(DETAILRENTAL.DETAIL_FEE) AS "RENTAL FEE REVENUE"
FROM MEMBERSHIP, RENTAL, DETAILRENTAL
WHERE MEMBERSHIP.MEM_NUM = RENTAL.MEM_NUM
AND RENTAL.RENT_NUM = DETAILRENTAL.RENT_NUM
GROUP BY MEMBERSHIP.MEM_NUM, MEM_LNAME, MEM_FNAME

/*Dæmi 23*/
SELECT MOVIE_NUM, MOVIE_GENRE, AVG(MOVIE_COST) AS "AVERAGE_COST", MOVIE_COST, (MOVIE_COST) AS "PERCENT DIFFERENCE"
FROM MOVIE
GROUP BY MOVIE_GENRE, MOVIE_NUM

