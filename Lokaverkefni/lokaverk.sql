CREATE DATABASE 2512982709_tinyvideo;

USE 2512982709_tinyvideo;

CREATE TABLE PRICE(
	PRICE_CODE INT NOT NULL,
	PRICE_DESCRIPTION VARCHAR(255),
	PRICE_RENTFEE DECIMAL(13, 2),
	PRICE_DAILYLATEFEE DECIMAL(13, 2),
	PRIMARY KEY(PRICE_CODE)
);
CREATE TABLE MOVIE(
	MOVIE_NUM INT NOT NULL PRIMARY KEY,
	MOVIE_TITLE VARCHAR(255),
	MOVIE_YEAR INT(4),
	MOVIE_COST DECIMAL(13, 2),
	MOVIE_GENRE VARCHAR(255),
	PRICE_CODE INT NOT NULL,
	FOREIGN KEY(PRICE_CODE) REFERENCES PRICE(PRICE_CODE)
);
CREATE TABLE VIDEO(
	VID_NUM INT(8) NOT NULL PRIMARY KEY,
	VID_INDATE DATE,
	MOVIE_NUM INT(8),
	FOREIGN KEY(MOVIE_NUM) REFERENCES MOVIE(MOVIE_NUM)
);
CREATE TABLE MEMBERSHIP(
	MEM_NUM INT(8) NOT NULL,
	MEM_FNAME VARCHAR(255) NOT NULL,
	MEM_LNAME VARCHAR(255) NOT NULL,
	MEM_STREET VARCHAR(255),
	MEM_CITY VARCHAR(255),
	MEM_STATE VARCHAR(2),
	MEM_ZIP INT(5),
	MEM_BALANCE INT,
	PRIMARY KEY(MEM_NUM)
);
CREATE TABLE RENTAL(
	RENT_NUM INT(8) NOT NULL ,
	RENT_DATE DATE,
	MEM_NUM INT(8),
	PRIMARY KEY(RENT_NUM),
	FOREIGN KEY(MEM_NUM) REFERENCES MEMBERSHIP(MEM_NUM)
);
CREATE TABLE DETAILRENTAL(
	RENT_NUM INT(8) NOT NULL,
	VID_NUM INT(8) NOT NULL,
	DETAIL_FEE DECIMAL(13, 2),
	DETAIL_DUEDATE DATE,
	DETAIL_RETURNDATE DATE,
	DETAIL_DAILYLATEFEE DECIMAL(13, 2),
	PRIMARY KEY(RENT_NUM, VID_NUM),
	FOREIGN KEY(RENT_NUM) REFERENCES RENTAL(RENT_NUM),
	FOREIGN KEY(VID_NUM) REFERENCES VIDEO(VID_NUM)
);

